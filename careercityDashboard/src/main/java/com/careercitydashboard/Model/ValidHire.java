package com.careercitydashboard.Model;

public class ValidHire {

    private String emp_id;
    private String first_name;
    private String last_name;
    private String previousJobProfile;
    private String newJobProfile;
    private String workType;
    private String method;
    private String dateUsed;

    public String getEmp_id() {

        return emp_id;

    }

    public void setEmp_id(String emp_id) {

        this.emp_id = emp_id;

    }

    public String getFirst_name() {

        return first_name;

    }

    public void setFirst_name(String first_name) {

        this.first_name = first_name;

    }

    public String getLast_name() {

        return last_name;

    }

    public void setLast_name(String last_name) {

        this.last_name = last_name;

    }

    public String getPreviousJobProfile() {

        return previousJobProfile;

    }

    public void setPreviousJobProfile(String previousJobProfile) {

        this.previousJobProfile = previousJobProfile;

    }

    public String getNewJobProfile() {

        return newJobProfile;

    }

    public void setNewJobProfile(String newJobProfile) {

        this.newJobProfile = newJobProfile;

    }

    public String getWorkType() {

        return workType;

    }

    public void setWorkType(String workType) {

        this.workType = workType;

    }

    public String getMethod() {

        return method;

    }

    public void setMethod(String method) {

        this.method = method;

    }

    public String getDateUsed() {

        return dateUsed;

    }

    public void setDateUsed(String dateUsed) {

        this.dateUsed = dateUsed;

    }

}
