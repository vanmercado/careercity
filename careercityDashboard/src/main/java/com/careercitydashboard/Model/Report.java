package com.careercitydashboard.Model;

public class Report {

    private String label;
    private Integer counts;

    public Report() {

    }

    public String getLabel() {

        return label;

    }

    public void setLabel(String label) {

        this.label = label;

    }

    public Integer getCounts() {

        return counts;

    }

    public void setCounts(Integer counts) {

        this.counts = counts;

    }

}
